	<x-markdown class="mt-12">
		## Fourth: I don’t like the app
		
		The default mastodon web client is kinda *meh* for sure. I never use it. There are **tons of amazing
		mastodon clients out there** for you to choose from. Here are a few options:
		
		- [Elk](https://elk.zone) is an awesome web-based client (no app to install!)
		that feels nicer to use and might be a little more familiar for Twitter expats.
		- [Ivory](https://tapbots.com/ivory/) is my mastodon client of choice. It's the spiritual
		successor to Tweetbot, so if you used their twitter app, you'll feel right at home in Ivory.
		Can't recommend it enough!
		- [Ice Cubes](https://github.com/Dimillian/IceCubesApp) is a great open source client.
		- [Mona](https://apps.apple.com/us/app/mona-for-mastodon/id1659154653) is another great client.
		If you used Spring for twitter, you'll feel right at home in Mona.
		- [Mammoth](https://getmammoth.app/) is a good option if you feel overwhelmed by the idea of
		finding new people to follow. It makes it easy to discover friends and interesting folks quickly.
		
		None of those sound right? There are [dozens more to choose from](https://joinmastodon.org/apps) if you
		want to really find the perfect fit!
		
		### Ugh, too many choices!
		
		[Just get Ivory](https://tapbots.com/ivory/) and be done with it. It's got great UX and is built
		by an amazing, small team of developers who have been making social media clients for years.
	</x-markdown>
	
	<x-markdown class="mt-12">
		## Fifth: I rely on Twitter for leads/business/etc
		
		![“Twitter FYP is getting more and more violently racist. Good thing I don’t rely on my network here to make a living.” – Daniel Coulbourne]({{ asset('images/tweets/twitter-fyp-racist.png') }})
		
		Yeah, that sucks. You probably have to continue to cross-post to Twitter. At least for a while
		longer. That said, there are huge opportunities to be a bigger fish on Mastodon. [Rand Fishkin
		put together a great summary during the most recent mass-exodus from Twitter](https://sparktoro.com/blog/why-marketers-creators-and-brands-should-start-using-mastodon-asap/).
		In it, he argues that:
		
		- It's easier to build a following now, when there are fewer leaders in each space
		- Engagement is way higher, so you need fewer followers to have reach
		- A small subset of your existing/potential audience is looking for you there already. If you're
		  not there, they'll find someone else to take your place
		- No algorithmic timeline means you don't have to play so many games to get exposure
	</x-markdown>
	
